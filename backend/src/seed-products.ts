async function main() {
  console.log("=== SS Traders Product Import ===\n");

  // 1. Upsert brand
  const brand = await prisma.brand.upsert({
    where: { name: BRAND_NAME },
    update: {},
    create: { name: BRAND_NAME },
  });
  console.log(`Brand: ${brand.name} (${brand.id})`);

  // 2. Upsert all categories
  const categoryNames = [...new Set(PRODUCTS.map((p) => p.category))];
  const categoryMap: Record<string, string> = {};
  for (const name of categoryNames) {
    const cat = await prisma.category.upsert({
      where: { name },
      update: {},
      create: { name },
    });
    categoryMap[name] = cat.id;
  }
  console.log(`Categories: ${categoryNames.length} upserted`);

  // 3. Insert products
  let inserted = 0, skipped = 0;
  for (const p of PRODUCTS) {
    const existing = await prisma.product.findFirst({ where: { name: p.name } });
    if (existing) { skipped++; continue; }

    const productCode = `AP-${String(p.sno).padStart(3, "0")}`;
    await prisma.product.create({
      data: {
        productCode,
        name: p.name,
        brandId: brand.id,
        categoryId: categoryMap[p.category],
        variant: p.packSize,
        unit: p.unit,
        hsnCode: p.hsnCode,
        gstPercentage: p.gstPercentage,
        sellingPrice: p.sellingPrice,
        purchasePrice: p.purchasePrice,
        stockQuantity: 0,
        minimumStock: 2,
      },
    });
    inserted++;
    if (inserted % 50 === 0) process.stdout.write(`  ${inserted} inserted...\n`);
  }

  console.log(`\nDone! Inserted: ${inserted}, Skipped (already exist): ${skipped}`);
  console.log("\n⚠  Selling prices are set to Indicative MRP from the Excel.");
  console.log("   Update your actual dealer prices in the Products page.");
  console.log("   Add stock quantities via the Inventory page.");
}

main()
  .catch((e) => { console.error("ERROR:", e); process.exit(1); })
  .finally(() => prisma.$disconnect());
